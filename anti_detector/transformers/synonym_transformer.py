# AI写作机械结构 - 需要打破
AI_STRUCTURAL_PATTERNS: List[dict] = [
    {"pattern": r"首先[,，].*?其次[,，].*?最后[,，]?", "type": "sequence"},
    {"pattern": r"第一[,，].*?第二[,，].*?第三[,，]?", "type": "sequence"},
    {"pattern": r"一方面[,，].*?另一方面[,，]?", "type": "contrast"},
    {"pattern": r"不仅[,，].*?而且[,，]?", "type": "addition"},
    {"pattern": r"既[,，].*?又[,，]?", "type": "addition"},
    {"pattern": r"虽然[,，].*?但是[,，]?", "type": "concession"},
    {"pattern": r"因为[,，].*?所以[,，]?", "type": "cause"},
    {"pattern": r"如果[,，].*?那么[,，]?", "type": "condition"},
]


class SynonymTransformer:
    """同义词替换转换器"""

    def __init__(self, intensity: float = 0.3, use_pos: bool = True):
        self.intensity = intensity
        self.use_pos = use_pos
        self._synonym_cache = {}

    def _get_pos_tag(self, word: str) -> str:
        """获取词性标记的首字符"""
        words = pseg.cut(word)
        for w, flag in words:
            if w == word:
                return flag[0] if flag else 'x'
        return 'x'

    def _get_replacement(self, word: str) -> str:
        """获取同义词替换"""
        key = (word, self.intensity)
        if key in self._synonym_cache:
            return self._synonym_cache[key]

        result = word

        if word in SYNONYM_DICT:
            replacements = SYNONYM_DICT[word]
            if replacements:
                result = random.choice(replacements)

        self._synonym_cache[key] = result
        return result

    def transform(self, text: str) -> str:
        """执行同义词替换"""
        if not text:
            return text

        words = pseg.cut(text)
        new_words = []

        for word, flag in words:
            if word.strip() and word in SYNONYM_DICT:
                if random.random() < self.intensity * 2:
                    replacement = self._get_replacement(word)
                    new_words.append(replacement)
                else:
                    new_words.append(word)
            else:
                new_words.append(word)

        result = ''.join(new_words)

        # 额外执行AI模式替换
        if random.random() < self.intensity * 1.5:
            result = self.replace_ai_patterns(result)

        return result

    def replace_ai_patterns(self, text: str) -> str:
        """替换AI高频写作模式"""
        result = text

        for pattern in AI_PATTERNS:
            if pattern in result:
                if random.random() < self.intensity * 0.5:
                    result = result.replace(pattern, "")
                    break

        return result

    def break_ai_structure(self, text: str) -> str:
        """打破AI写作的机械结构"""
        import re

        for pattern_def in AI_STRUCTURAL_PATTERNS:
            pattern = pattern_def["pattern"]
            match = re.search(pattern, text)
            if match and random.random() < self.intensity * 0.3:
                matched_text = match.group()
                if pattern_def["type"] == "sequence":
                    # 随机删除或打乱部分
                    parts = re.split(r'[,，]', matched_text)
                    if len(parts) >= 3:
                        random.shuffle(parts[1:])
                        result = '，'.join(parts)
                        text = text.replace(matched_text, result)
                elif pattern_def["type"] == "contrast":
                    # 替换对比结构
                    text = text.replace(matched_text, "但")
                elif pattern_def["type"] == "addition":
                    text = text.replace(matched_text, "并且")
                elif pattern_def["type"] == "cause":
                    text = text.replace(matched_text, "所以")
                break

        return text
